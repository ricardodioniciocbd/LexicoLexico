# Programa Básico en MiniLang
# Demuestra variables y operaciones aritméticas

x = 10
y = 5

suma = x + y
resta = x - y
multiplicacion = x * y
division = x / y

print("Operaciones Básicas")
print("x = " + str(x))
print("y = " + str(y))
print("x + y = " + str(suma))
print("x - y = " + str(resta))
print("x * y = " + str(multiplicacion))
print("x / y = " + str(division))
